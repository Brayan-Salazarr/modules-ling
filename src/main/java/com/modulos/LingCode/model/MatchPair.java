package com.modulos.LingCode.model;

public class MatchPair {
    private String left;
    private String right;

    public MatchPair(String left, String right) {
        this.left = left;
        this.right = right;
    }

    public String getLeft() {
        return left;
    }

    public String getRight() {
        return right;
    }
}
